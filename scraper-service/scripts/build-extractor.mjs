// Bundles the canonical extractor (extension/src/content/extractor.ts) into a
// plain-JS string that can be injected into a Playwright page via addScriptTag.
//
// The extractor is pure DOM code, so the scraper service reuses it verbatim
// instead of duplicating selectors. Run automatically before start/dev.

import ts from 'typescript'
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const SRC  = resolve(here, '../../extension/src/content/extractor.ts')
const OUT  = resolve(here, '../src/generated/extractor-source.ts')

let source = readFileSync(SRC, 'utf8')

// Strip the type-only import (no runtime effect) and the `export` keywords so
// the result is a self-contained script we can eval inside the page.
source = source
  .replace(/^import type .*$/m, '')
  .replace(/^export /gm, '')

// transpileModule only strips types; it does not type-check or resolve imports.
const js = ts.transpileModule(source, {
  compilerOptions: { target: ts.ScriptTarget.ES2019, removeComments: false },
}).outputText

// Expose the master extractor on the page's global scope.
const bundle = `${js}\n;globalThis.__extract = extractBusinessData;\n`

mkdirSync(dirname(OUT), { recursive: true })
writeFileSync(
  OUT,
  `// AUTO-GENERATED by scripts/build-extractor.mjs — do not edit.\n` +
  `// Source: extension/src/content/extractor.ts\n` +
  `export const EXTRACTOR_SOURCE = ${JSON.stringify(bundle)}\n`,
)

console.log(`[build-extractor] wrote ${OUT} (${bundle.length} bytes)`)
